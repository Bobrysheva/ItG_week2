package ru.karian.calculator.util;

public class Calculator {
    public static int countSum(int a, int b) {
        int sum = a + b;
        return sum;
    }

    public static int countResidual(int a, int b) {
        int residual = a - b;
        return residual;
    }

    public static int countProduct(int a, int b) {
        int product = a * b;
        return product;
    }
}
